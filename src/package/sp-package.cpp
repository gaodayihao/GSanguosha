class Yicong: public DistanceSkill{
public:
    Yicong():DistanceSkill("yicong"){

    }

    virtual int getCorrect(const Player *from, const Player *to) const{
        int correct = 0;
        if(from->hasSkill(objectName()) && from->getHp() > 2)
            correct --;
        if(to->hasSkill(objectName()) && to->getHp() <= 2)
            correct ++;

        return correct;
    }
};

class Yicongeff: public TriggerSkill{
public:
  Yicongeff():TriggerSkill("#yicongeff"){
        frequency = Compulsory;
        events << HpChanged
               << GameStart;
    }

   virtual bool triggerable(const ServerPlayer *target) const{
       return TriggerSkill::triggerable(target);
   }

    virtual bool trigger(TriggerEvent event, ServerPlayer *player, QVariant &data) const{
        Room *room = player->getRoom();
        if(event == GameStart){
            if(player->getHp()>2)
                player->addMark("ycg");
            else
                player->addMark("ycs");
        }
        if (player->getHp()>2 && player->getMark("ycg") == 0){
            room->playSkillEffect("yicong",1);
            player->addMark("ycg");
            player->removeMark("ycs");
        }
        else if(player->getHp()<3 && player->getHp()>0 && player->getMark("ycs") ==0){
            room->playSkillEffect("yicong",2);
            player->addMark("ycs");
            player->removeMark("ycg");
        }
        return false;
    }
};

class Xiuluo: public PhaseChangeSkill{
public:
    Xiuluo():PhaseChangeSkill("xiuluo"){

    }

    virtual bool triggerable(const ServerPlayer *target) const{
        return PhaseChangeSkill::triggerable(target)
                && target->getPhase() == Player::Start
                && !target->isKongcheng()
                && !target->getJudgingArea().isEmpty();
    }

    virtual bool onPhaseChange(ServerPlayer *target) const{
        bool once_success = false;
        do{
            once_success = false;

            if(!target->askForSkillInvoke(objectName()))
                return false;

            Room *room = target->getRoom();
            int card_id = room->askForCardChosen(target, target, "j", objectName());
            const Card *card = Sanguosha->getCard(card_id);

            QString suit_str = card->getSuitString();
            QString pattern = QString(".%1").arg(suit_str.at(0).toUpper());
            QString prompt = QString("@xiuluo:::%1").arg(suit_str);
            if(room->askForCard(target, pattern, prompt, QVariant(), CardDiscarded)){
                room->throwCard(card);
                once_success = true;
            }
        }while(!target->getCards("j").isEmpty() && once_success);

        return false;
    }
};

